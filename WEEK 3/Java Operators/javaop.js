// Arithmetic Operators are things like
//  + - * /
// Power of ** (2** = 4)
// + supports concatenating

// Decrement / Increment, -- / ++
let n = 100;
--n;
console.log(n); // 99
++n;
console.log(n); // 100

console.log(--n); // 99

console.log(n--); // (99, BUT AFTER IT IS PRINTED IT IS 98)
console.log(n); // 98

// they turn values into different values

// Modulus % (remainder)
// console.log(5 % 2); = 1, because 1 is the remainder
// allows you to get the remainder

// Assignment operators
let value = 124 / 2; // 62
value = value * 2; // 124
value *= 2; // 124
value /= 2; //31
console.log(value);
